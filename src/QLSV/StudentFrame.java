    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        // TODO add your handling code here:
        String full_name = txt_full_name.getText();
        String student_id = txt_student_id.getText();
        String date_of_birth = txt_date_of_birth.getText();
        String gender = (String) cbx_gender.getSelectedItem();
        String classroom = txt_classroom.getText();

        if (full_name.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên không được để trống.");
        }
        if (student_id.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã Sinh Viên không được để trống.");
        }
        if (date_of_birth.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa Nhập ngày sinh.");
        }
        if (classroom.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa Nhập lớp học.");
        }
        if (!full_name.equals("") && !student_id.equals("") && !date_of_birth.equals("") && !classroom.equals("")) {
            Student std = new Student(full_name, student_id, date_of_birth, gender, classroom);
            StudentModify.insert(std);
            loadData(cbx_classroom.getSelectedItem().toString());
        }

    }//GEN-LAST:event_btn_SaveActionPerformed

