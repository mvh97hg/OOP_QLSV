    private void loadData(String cls) {
        if (cls == "Tất cả") {
            List<Student> studentList = StudentModify.getALL();
            tableModel = (DefaultTableModel) tbl_Student.getModel();
            tableModel.setRowCount(0);
            studentList.forEach((student) -> {
                tableModel.addRow(new Object[]{tableModel.getRowCount() + 1, student.getFull_name(), student.getStudent_id(), student.getDate_of_birth(),
                    student.getGender(), student.getClassroom()});
            });
        } else {
            List<Student> studentList = StudentModify.getStudentClass(cls);
            tableModel = (DefaultTableModel) tbl_Student.getModel();
            tableModel.setRowCount(0);
            studentList.forEach((student) -> {
                tableModel.addRow(new Object[]{tableModel.getRowCount() + 1, student.getFull_name(), student.getStudent_id(), student.getDate_of_birth(),
                    student.getGender(), student.getClassroom()});
            });
        }
    }

